NodeWebSqlPouch.valid = function () {
  return true;
};
NodeWebSqlPouch.use_prefix = false; // no prefix necessary in Node

function WebSqlPouchPlugin (PouchDB) {
  PouchDB.adapter('websql', NodeWebSqlPouch, true);
}

